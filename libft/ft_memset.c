#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	size_t	i;
	char	*cpy;

	i = 0;
	cpy = (char *)str;
	while (i < n)
	{
		cpy[i] = c;
		i++;
	}
	return (cpy);
}
